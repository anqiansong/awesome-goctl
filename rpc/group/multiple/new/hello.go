package main

import (
	"flag"
	"fmt"

	"new/internal/config"
	helloserviceServer "new/internal/server/helloservice"
	hiserviceServer "new/internal/server/hiservice"
	"new/internal/svc"
	"new/pb"

	"github.com/zeromicro/go-zero/core/conf"
	"github.com/zeromicro/go-zero/core/service"
	"github.com/zeromicro/go-zero/zrpc"
	"google.golang.org/grpc"
	"google.golang.org/grpc/reflection"
)

var configFile = flag.String("f", "etc/hello.yaml", "the config file")

func main() {
	flag.Parse()

	var c config.Config
	conf.MustLoad(*configFile, &c)
	ctx := svc.NewServiceContext(c)

	s := zrpc.MustNewServer(c.RpcServerConf, func(grpcServer *grpc.Server) {
		pb.RegisterHelloServiceServer(grpcServer, helloserviceServer.NewHelloServiceServer(ctx))
		pb.RegisterHiServiceServer(grpcServer, hiserviceServer.NewHiServiceServer(ctx))

		if c.Mode == service.DevMode || c.Mode == service.TestMode {
			reflection.Register(grpcServer)
		}
	})
	defer s.Stop()

	fmt.Printf("Starting rpc server at %s...\n", c.ListenOn)
	s.Start()
}
